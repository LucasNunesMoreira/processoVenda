package dev.lucas.procss.models;

public class ObjetoFinal {

    private String nomeVendedor;
    private long totalVendas;
    private long mediaDiaria;


    public String getNomeVendedor() {
        return nomeVendedor;
    }

    public void setNomeVendedor(String nomeVendedor) {
        this.nomeVendedor = nomeVendedor;
    }

    public long getTotalVendas() {
        return totalVendas;
    }

    public void setTotalVendas(long totalVendas) {
        this.totalVendas = totalVendas;
    }

    public long getMediaDiaria() {
        return mediaDiaria;
    }

    public void setMediaDiaria(long mediaDiaria) {
        this.mediaDiaria = mediaDiaria;
    }
}
